package Constructors;

public class Parameterized_constructor {
	 Parameterized_constructor(){
		 System.out.println("Default constructor");
  	}
	 Parameterized_constructor(int i, int j)
    {
   System.out.println("constructor with Two parameters");
 	}
	 Parameterized_constructor(int i, int j, int k)
	 {
   System.out.println("constructor with Three parameters");	      
  	}
	 Parameterized_constructor(int i, String name)
	 {
	       System.out.println("constructor with int and String param");
	 }

}


